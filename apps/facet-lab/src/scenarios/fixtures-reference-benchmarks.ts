function action(
  id: string,
  name: string,
  preset = "secondaryAction",
  style: Omit<BoxStyle, "preset"> = {},
): FacetTree["nodes"][string] {
  return {
    id,
    type: "box",
    children: [`${id}-label`],
    onPress: { kind: "agent", name },
    style: { preset, ...style },
  };
}

function actionLabel(id: string, value: string): FacetTree["nodes"][string] {
  return { id: `${id}-label`, type: "text", value, style: { preset: "actionLabel" } };
}

